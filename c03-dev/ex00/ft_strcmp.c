int	ft_strcmp(char *s1, char *s2)
{
	int	tally;

	tally = 0;
	while (*s1)
	{
		tally += *s1;
		s1++;
	}
	while (*s2)
	{
		tally -= *s2;
		s2++;
	}
	if (tally > 0)
		return (1);
	if (tally < 0)
		return (-1);
	return (0);
}
