#include <unistd.h>

void	ft_print_comb(void)
{
	char	ijk[3];

	ijk[0] = '0' - 1;
	while (++ijk[0] <= '7')
	{
		ijk[1] = ijk[0] + 1 - 1;
		while (++ijk[1] <= '8')
		{
			ijk[2] = ijk[1] + 1 - 1;
			while (++ijk[2] <= '9')
			{
				write(1, &ijk, 3);
				if (ijk[0] != '7')
				{
					write(1, ", ", 2);
				}
			}
		}
	}
}
